def check(D, solution, valid):
    """Checks if a given solution is correct for a list of deals.

    Args:
        D: A list of deals.
        solution: A list of commodities to exchange in a particular order.
        valid: Should solution not be None?

    Returns:
        If the given solution produces infinite money.
    """

    if not valid:
        return solution is None

    # A solution with 0, 1 or items is always incorrect.
    if len(solution) < 2:
        return False

    # Create a lookup table to quickly find the exchange rate
    # for a deal.
    ratios = {}

    for A, x, B, y in D:
        if A not in ratios:
            ratios[A] = {}

        ratios[A][B] = y / x

    # We start with 1, and we push this through the solution chain.
    current_items = 1.0

    for u, v in zip(solution[:-1], solution[1:]):
        # Cycle doesn't even have a valid path.
        if u not in ratios or v not in ratios[u]:
            return False

        current_items = current_items * ratios[u][v]

    # Lastly, we exchange the last item for the first.
    current_items = current_items * ratios[solution[-1]][solution[0]]

    return current_items > 1.0


